        int price=Integer.parseInt(jTextField9.getText());
        int quantity=Integer.parseInt(jTextField10.getText());
        int pId=Integer.parseInt(jTextField7.getText());
        int total = price * quantity;
        int stock=0;
        if(finalTotal==0)
        {
            String contactNo=jTextField2.getText().trim();
            try
            {
                Connection con=ConnectionProvider.getCon();
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("select state from buyers where contactNo='"+contactNo+"'");
                if(rs.next())
                {
                    state=rs.getString(1);
                }
                
            }
            catch(Exception e){}
        }
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs2=st.executeQuery("select stock from products where productNumber='"+pId+"'");
                if(rs2.next())
                {
                    stock = Integer.parseInt(rs2.getString(1));
                }
        }
        catch(Exception e){}
        if(stock>=quantity)
        {
            DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
            model.addRow(new Object[]{jTextField7.getText(),jTextField8.getText(),jTextField9.getText(),jTextField10.getText(),total});
        
            finalTotal=finalTotal+total;
            String finalTotal1=String.valueOf(finalTotal);
            jLabel26.setText("  " + finalTotal1);
        
            if(state.equals("Gujarat"))
            {
                CGST = finalTotal*9 / 100;
                SGST = finalTotal*9 / 100;
                IGST = 0;
                jLabel28.setText("  " + String.valueOf(CGST));
                jLabel24.setText("  " + String.valueOf(SGST));
                jLabel25.setText("  0");
            }
            else
            {
                CGST = 0;
                SGST = 0;
                IGST = finalTotal*18 / 100;
                jLabel28.setText("  0");
                jLabel24.setText("  0");
                jLabel25.setText("  " + String.valueOf(IGST));
            }
        
            finalTotalToPay = finalTotal + CGST + SGST + IGST ;
            roundOff = finalTotalToPay - (int)finalTotalToPay;
            finalTotalToPay = (int)finalTotalToPay;
        
            jLabel32.setText(" -" + String.valueOf(String.format("%.2f", roundOff)));
            jLabel35.setText("  " + String.valueOf(finalTotalToPay));
            
            try
            {
                int remainingStock=stock-quantity;
                Connection con=ConnectionProvider.getCon();
                Statement st=con.createStatement();
                st.executeUpdate("UPDATE products set stock = '"+remainingStock+"' where productNumber='"+pId+"'");
            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null,e);
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Not Enough stock to add");
        }
