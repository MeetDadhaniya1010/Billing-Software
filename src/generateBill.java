    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        try
        {
            if(name.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Enter Buyer Name");
            }
            else
            {
                Connection con=ConnectionProvider.getCon();
                Statement st=con.createStatement();
                ResultSet rs=st.executeQuery("select *from buyers where name like '"+name+"%'");
                if(rs.next())
                {
                    jTextField1.setText("  " + rs.getString(1));
                    jTextField2.setText("  " + rs.getString(2));
                    jTextField3.setText("  " + rs.getString(3));
                    jTextField4.setText("  " + rs.getString(4));
                    jTextField5.setText("  " + rs.getString(5));
                    jTextField6.setText("  " + rs.getString(6));
                    jTextField12.setText("  " + rs.getString(7));
                    
                    jTextField7.requestFocus();
                    jTextField1.setEditable(false);
                    jTextField2.setEditable(false);
                    jTextField3.setEditable(false);
                    jTextField4.setEditable(false);
                    jTextField5.setEditable(false);
                    jTextField6.setEditable(false);
                    jTextField12.setEditable(false);
                }
                else
                {
                    JOptionPane.showMessageDialog(null,"Buyer not found add new buyer...");
                    setVisible(false);
                    new newBuyer().setVisible(true);
                }
            }
        }
        catch(Exception e)
        {
                JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

