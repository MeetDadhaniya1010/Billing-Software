    public static String numberToWords(long num) {
    if (num == 0) {
        return "Zero";
    }

    String[] belowTwenty = {
        "", "One", "Two", "Three", "Four", "Five", "Six", "Seven", "Eight", "Nine", "Ten",
        "Eleven", "Twelve", "Thirteen", "Fourteen", "Fifteen", "Sixteen", "Seventeen", "Eighteen", "Nineteen"
    };
    String[] tens = {"", "", "Twenty", "Thirty", "Forty", "Fifty", "Sixty", "Seventy", "Eighty", "Ninety"};
    String[] thousands = {"", "Thousand", "Million", "Billion", "Trillion"};

    String result = "";
    int thousandIndex = 0;

    while (num > 0) {
        if (num % 1000 != 0) {
            result = helper((int) (num % 1000), belowTwenty, tens) + thousands[thousandIndex] + " " + result;
        }
        num /= 1000;
        thousandIndex++;
    }

    return result.trim();
}

private static String helper(int num, String[] belowTwenty, String[] tens) {
    if (num == 0) {
        return "";
    } else if (num < 20) {
        return belowTwenty[num] + " ";
    } else if (num < 100) {
        return tens[num / 10] + " " + helper(num % 10, belowTwenty, tens);
    } else {
        return belowTwenty[num / 100] + " Hundred " + helper(num % 100, belowTwenty, tens);
    }
}

